import prisma from "../configs/prisma.js";

const getUserWorkspaces = async (req, res) => {
  try {
    const { userId } = await req.auth();
    const workspaces = await prisma.workspace.findMany({
      where: {
        members: {
          some: { userId },
        },
      },
      include: {
        members: { include: { user: true } },
        projects: {
          include: {
            tasks: {
              include: {
                assignee: true,
                comments: { include: { user: true } },
              },
            },
            members: { include: { user: true } },
          },
        },
        owner: true,
      },
    });

    res.status(200).json({ suceess: true, workspaces });
  } catch (error) {
    console.log(error);
    res.status(500).json({
      message: error.code || error.message,
    });
  }
};

const addMemebr = async (req, res) => {
  try {
    const { userId } = await req.auth();
    const { email, role, workspaceId, message } = req.body;

    const user = await prisma.user.findUnique({ where: { email } });

    if (!user) {
      return res
        .status(404)
        .json({ success: false, message: "User not found" });
    }

    if (!workspaceId || !role) {
      return res.status(400).json({
        success: false,
        message: "missing required parameters",
      });
    }

    if (!["ADMIN", "MEMBER"].includes(role)) {
      return res.status(403).json({
        success: false,
        message: "invalid role",
      });
    }

    //fetch workspace

    const workspace = await prisma.workspace.findUnique({
      where: { id: workspaceId },
      include: { members: true },
    });

    if (!workspace) {
      return res.status(404).json({
        success: false,
        message: "workspace not found",
      });
    }

    // check if creator has admin role

    if (
      !workspace.members.find(
        (member) => member.userId === userId && member.role === "ADMIN",
      )
    ) {
      return res.status(401).json({
        success: false,
        message: "You don't have admin priviledges",
      });
    }

    // check if user is already a member

    const existingMember = workspace.members.find(
      (member) => member.userId === userId,
    );

    if (existingMember) {
      return res.status(400).json({
        success: false,
        message: "User is already a member",
      });
    }

    const member = await prisma.workspaceMember.create({
      data: {
        userId: user.id,
        workspaceId,
        role,
        message,
      },
    });

    res
      .status(200)
      .json({ success: true, message: "member added successfully", member });
  } catch (error) {
    console.log(error);
    res.status(500).json({ message: error.message });
  }
};

export { getUserWorkspaces, addMemebr };
